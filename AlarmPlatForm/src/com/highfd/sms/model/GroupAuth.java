package com.highfd.sms.model;

public class GroupAuth {

	/**
	 * 权限类型
	 */
	public String grounpId;
	
	public String zone_id;
	public String dic_id;
	
	/**
	 * 台站列表  ,分割
	 */
	public String siteNumberStr;
	
	
	public GroupAuth() {
		super();
	}
	public String getGrounpId() {
		return grounpId;
	}
	public void setGrounpId(String grounpId) {
		this.grounpId = grounpId;
	}
	public String getZone_id() {
		return zone_id;
	}
	public void setZone_id(String zoneId) {
		zone_id = zoneId;
	}
	public String getDic_id() {
		return dic_id;
	}
	public void setDic_id(String dicId) {
		dic_id = dicId;
	}
	public String getSiteNumberStr() {
		return siteNumberStr;
	}
	public void setSiteNumberStr(String siteNumberStr) {
		this.siteNumberStr = siteNumberStr;
	}

}
