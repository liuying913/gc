package com.highfd.sms.model;

public class SitePerson {

	public String siteNumber;
	public String sitePhone;
	public String siteProPhone;
	public String siteChaPhone;
	public String smsMessage;
	
	public String eventId;



	public String getSiteNumber() {
		return siteNumber;
	}

	public void setSiteNumber(String siteNumber) {
		this.siteNumber = siteNumber;
	}

	public String getSitePhone() {
		return sitePhone;
	}

	public void setSitePhone(String sitePhone) {
		this.sitePhone = sitePhone;
	}

	public String getSiteProPhone() {
		return siteProPhone;
	}

	public void setSiteProPhone(String siteProPhone) {
		this.siteProPhone = siteProPhone;
	}

	public String getSiteChaPhone() {
		return siteChaPhone;
	}

	public void setSiteChaPhone(String siteChaPhone) {
		this.siteChaPhone = siteChaPhone;
	}

	public String getSmsMessage() {
		return smsMessage;
	}

	public void setSmsMessage(String smsMessage) {
		this.smsMessage = smsMessage;
	}

	public String getEventId() {
		return eventId;
	}

	public void setEventId(String eventId) {
		this.eventId = eventId;
	}




}
