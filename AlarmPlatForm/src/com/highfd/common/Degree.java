package com.highfd.common;

public class Degree {
	
	private String webCode;//网站上面的编号
	
	private String oracleCode;//数据库存储编号
	private String name;//中文名称
	
	public Degree() {
		super();
	}
	
	public Degree(String webCode, String oracleCode, String name) {
		super();
		this.webCode = webCode;
		this.oracleCode = oracleCode;
		this.name = name;
	}

	public String getWebCode() {
		return webCode;
	}
	public void setWebCode(String webCode) {
		this.webCode = webCode;
	}
	public String getOracleCode() {
		return oracleCode;
	}
	public void setOracleCode(String oracleCode) {
		this.oracleCode = oracleCode;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
